package dat.startcode.model.entities;

public class ToolshedWidth {

    private int toolshedWidthID;
    private int toolshedWidth;

    public ToolshedWidth(int toolshedWidthID, int toolshedWidth) {
        this.toolshedWidthID = toolshedWidthID;
        this.toolshedWidth = toolshedWidth;
    }

    public int getToolshedWidthID() {
        return toolshedWidthID;
    }

    public int getToolshedWidth() {
        return toolshedWidth;
    }
}
